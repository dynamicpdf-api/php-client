<?php
namespace DynamicPDF\Api;


/**
 *
 * Represents a page information.
 *
 */
class PageInformation
{

    /**
     *
     * Gets or sets the page number.
     *
     */
    public $PageNumber;

    /**
     *
     * Gets or sets the width of the page.
     *
     */
    public $Width;

    /**
     *
     * Gets or sets the height of the page.
     *
     */
    public $Height;
}
