<?php
    /// <summary>
    /// Represents information of a text field.
    /// </summary>
    class TextFieldInformation
    {
        /// <summary>
        /// Gets or Sets the name of the Text field.
        /// </summary>
        public $Name;

        /// <summary>
        /// Gets or Sets the value of the Text field.
        /// </summary>
        public $Value;

        /// <summary>
        /// Gets or Sets the default value of the Text field.
        /// </summary>
        public $DefaultValue;
    }
?>